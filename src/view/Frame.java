    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        
        if(Odbiorca.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Musisz podać Odbiorcę!", "ZŁE DANE", WIDTH);
        }else{
        if(rob2 == 100){
            JOptionPane.showMessageDialog(null, "Usuń wiadomości ze skrzynki aby móc wysłać wiadomość.", "SKRZYNKA PEŁNA!", WIDTH);
        }
        else{
            rob2++;
            Date DataWys = new Date();
        WiadomosciWyslaneDane ww3 = new WiadomosciWyslaneDane(Odbiorca.getText(), jTextField2.getText(), Odbiorca.getText(),
               "localhost", 1245, DataWys, jTextArea1.getText() , jLabel11.getText());
        lWiad.dodajWiadomosc(ww3);
        
        for(int i = 0; i<lm.getSize(); i++){
            lm.clear();
        }
        
        String[] oknowiad = lWiad.Info();
       for(String info : oknowiad){
            lm.addElement(info);
        }
        jButton4.setEnabled(true);
       jButton3.setEnabled(false);
       
         jLabel7.setText(String.valueOf(rob2));
         jButton3.setText("Wysłane" + "(" + rob2 +")");
        }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void TrashButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TrashButtonActionPerformed

        int index;
           if(wiadomosci.isSelectionEmpty()){
               return;
           }
            
        else if(jButton3.isEnabled()){
            index = wiadomosci.getSelectedIndex();
            rob1--;
            lm.remove(index);
            lOdebWiad.usunWiadomosci(index);
            String xd = String.valueOf(rob1);
            jButton4.setText("Odebrane" + "(" + xd +")");
            jLabel7.setText(String.valueOf(rob1));
           }
           else{
            index = wiadomosci.getSelectedIndex();
            rob2--;
            lm.remove(index);
            lWiad.usunWiadomosci(index);
            String xd = String.valueOf(rob2);
            jButton3.setText("Odebrane" + "(" + xd +")");
            jLabel7.setText(String.valueOf(rob2));
            }
    }//GEN-LAST:event_TrashButtonActionPerformed

