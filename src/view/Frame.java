    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        JFileChooser sav = new JFileChooser();
            if(sav.showSaveDialog(this)== JFileChooser.APPROVE_OPTION){
                File f = sav.getSelectedFile();
                try {
                    try (FileWriter fw = new FileWriter(f)) {
                        BufferedWriter bw = new BufferedWriter(fw);
                        bw.append(jTextField5.getText());
                        bw.newLine();
                        bw.append(jTextField11.getText());
                        bw.newLine();
                        bw.newLine();
                        bw.append("Treść: ");
                        bw.newLine();
                        bw.append(jTextWiadomosci.getText());
                        bw.newLine();
                        bw.append(jLabel16.getText());
                        
                        
                        bw.close();
                    }
                }catch (IOException ex){} 
            }
    }//GEN-LAST:event_jButton12ActionPerformed

